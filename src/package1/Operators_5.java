package package1;

public class Operators_5 {

	public static void main(String[] args) 
	{
		//Assignment and Shorthand operators
		int a=15; //assignment
		
		a+=2;
		System.out.println(a);
		
		a-=2;
		System.out.println(a);
		
		a*=3;
		System.out.println(a);
		
		a/=3;
		System.out.println(a);
		
		a%=2;
		System.out.println(a);
		

	}

}
