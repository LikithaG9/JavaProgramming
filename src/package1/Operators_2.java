package package1;

public class Operators_2 {

	public static void main(String[] args)
	{
		int a=100,b=20;
		
		//Relational operators
		System.out.println(a>b);
		System.out.println(a<b);
		a=20;
		System.out.println(a>=b);
		System.out.println(a<=b);
		System.out.println(a!=b);
		System.out.println(a==b);

	}

}
