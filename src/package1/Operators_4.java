package package1;

public class Operators_4 
{
	public static void main(String[] args)
	{
		//Increment operators
		
				int p=10,q=10,res;
				
				++p;
				q++;
				
				System.out.println(p);
				System.out.println(q);
				
				res=++p;
				System.out.println(res);
				System.out.println(p);
				
				res=q++;
				System.out.println(res);
				System.out.println(q);
				
				//Decrement operators
						
			    --p;
				q--;
						
				System.out.println(p);
				System.out.println(q);
						
				res=--p;
				System.out.println(res);
				System.out.println(p);
						
				res=q--;
				System.out.println(res);
				System.out.println(q);
				
	}

}
