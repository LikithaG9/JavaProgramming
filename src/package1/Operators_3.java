package package1;

public class Operators_3 {

	public static void main(String[] args) 
	{
		int a=100,b=20;
		//Logical operators
				boolean x=true,y=false;
				System.out.println(x&&y);
				System.out.println(x||y);
				System.out.println(!y);
				
				System.out.println((a>b)&&(a<b));
				System.out.println((a>b)||(a<b));
				System.out.println(!(a>b));

	}

}
